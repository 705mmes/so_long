#include "libft.h"

int	ft_putchar(char c)
{
	if (write(1, &c, 1) == -1)
		return (-1);
	return (1);
}

int	ft_putchar2(char c)
{
	if (write(1, &c, 1) == -1)
		return (-2147483648);
	return (1);
}
