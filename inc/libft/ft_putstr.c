#include "libft.h"

int	ft_putstr(char *s)
{
	int			i;

	i = 0;
	if (!s)
	{
		if (write(1, "(null)", 6) == -1)
			return (-1);
		return (6);
	}
	while (s[i])
		if (write(1, &s[i++], 1) == -1)
			return (-1);
	return (ft_strlen(s));
}
